df <- data.frame(table$Y)

log_g <- function(a,b){
  
  sm <- 1

  for (i in 1:30){
    for (j in 2:25){
      
      prob <- log(a)*(sum(df[1:j-1,i])) + log(b)*(j-1-sum(df[1:j-1,i]))
      
      sm <- sm + df[j,i]*prob + (1-df[j,i])*log(1-exp(prob))
    }
  }
  return(sm)
}


