  log_alpha <- min(1,log_g(theta[i+1,1],theta[i+1,2])/log_g(theta[i,1],theta[i,2]))
  
  u <- runif(1)
  
  
  if(log_alpha < log(u)){
    theta[i+1,] <- theta[i,]}
